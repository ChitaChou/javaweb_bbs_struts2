package com.discovery.model;

public class Forum {
	String forum_id;
	String forum_name;
	String forum_desc;
	String by_area_id;
	public String getForum_id() {
		return forum_id;
	}
	public void setForum_id(String forum_id) {
		this.forum_id = forum_id;
	}
	public String getForum_name() {
		return forum_name;
	}
	public void setForum_name(String forum_name) {
		this.forum_name = forum_name;
	}
	public String getForum_desc() {
		return forum_desc;
	}
	public void setForum_desc(String forum_desc) {
		this.forum_desc = forum_desc;
	}
	public String getBy_area_id() {
		return by_area_id;
	}
	public void setBy_area_id(String by_area_id) {
		this.by_area_id = by_area_id;
	}
	
	
}
