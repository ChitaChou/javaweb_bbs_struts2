package com.discovery.model;

public class Reply {
	String reply_id;
	String reply_writer_id;
	String by_thread_id;
	String reply_time;
	String reply_text;
	String second_reply_id;
	public String getReply_id() {
		return reply_id;
	}
	public void setReply_id(String reply_id) {
		this.reply_id = reply_id;
	}
	public String getReply_writer_id() {
		return reply_writer_id;
	}
	public void setReply_writer_id(String reply_writer_id) {
		this.reply_writer_id = reply_writer_id;
	}
	public String getBy_thread_id() {
		return by_thread_id;
	}
	public void setBy_thread_id(String by_thread_id) {
		this.by_thread_id = by_thread_id;
	}
	public String getReply_time() {
		return reply_time;
	}
	public void setReply_time(String reply_time) {
		this.reply_time = reply_time;
	}
	public String getReply_text() {
		return reply_text;
	}
	public void setReply_text(String reply_text) {
		this.reply_text = reply_text;
	}
	public String getSecond_reply_id() {
		return second_reply_id;
	}
	public void setSecond_reply_id(String second_reply_id) {
		this.second_reply_id = second_reply_id;
	}
	
	
}
